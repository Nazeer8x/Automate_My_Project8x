package sept.ex_16092024;

public class Lab012_ArithmaticOperator {
    public static void main(String[] args) {
        //Arithmatic operator  +, -, *, / , %
        int a = 200;
        int b = 200;
        System.out.println(a+b); // + -> binary -> 2 operands
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        // % modulus - it gives remainder
        System.out.println(a%b);
    }
}
