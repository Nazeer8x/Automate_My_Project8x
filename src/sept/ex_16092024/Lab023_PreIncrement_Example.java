package sept.ex_16092024;

public class Lab023_PreIncrement_Example {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(a); // 11
        System.out.println(result); //11
        //line no. | expression | a
        //5 |                    | 10
        //6 | result = 11 | 11
        //7 | result = 11 | 11
    }
}
