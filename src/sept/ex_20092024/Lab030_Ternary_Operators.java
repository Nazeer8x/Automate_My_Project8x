package sept.ex_20092024;

public class Lab030_Ternary_Operators {
    public static void main(String[] args) {
        //condition ? expression_if_true : expression_if_false
        int a1 = (30>40) ? 10 : 20; // if 30>40 then if answer is true then 10 will be output and if answer is false then 20 will be output
        System.out.println(a1);
    }
}
